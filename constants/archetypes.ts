/**
 * Get archetype by ID
 */
export const getArchetypeById = (id: string): Archetype | undefined => {
  return ARCHETYPES.find(a => a.id === id);
};

/**
 * Get archetype colors for chart visualization
 */
export const getArchetypeColors = (): string[] => {
  return ARCHETYPES.map(a => a.color);
};

/**
 * Get archetype names for chart labels
 */
export const getArchetypeNames = (): string[] => {
  return ARCHETYPES.map(a => a.name);
};
